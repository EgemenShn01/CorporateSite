document.querySelector('.animate').addEventListener('click', function(e) {
    e.preventDefault(); // Linkin varsayılan davranışını engeller

    const section = document.querySelector('#home'); // Kaydırılacak bölümü seç
    section.scrollIntoView({ behavior: 'smooth' }); // Yumuşak kaydırma efektini uygula
});
